
1. #Clone the repository:
    git clone <repository-url>

2. #Navigate to the project directory:
    
    cd MagicalArena
    
3. #Compile the project:
    
    mvn clean compile
    
4. #Run the project:
    
    mvn exec:java -Dexec.mainClass="arena.Main"
    
5. #To run the tests:
    
    mvn test
    
